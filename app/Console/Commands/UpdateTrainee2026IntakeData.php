    {
        $dry = $this->option('dry-run');
        if ($dry) {
            $this->warn('[DRY RUN] No changes will be written.');
        }

        $updated  = 0;
        $notFound = 0;
        $isVerbose = $this->output->isVerbose();

        foreach (self::DATA as $pen => $d) {
            [$invAmt, $feePaid, $invStatus, $amtPaid, $mop, $payDate, $sponsor] = $d;

            $trainees = DB::table('trainees')
                ->whereRaw('TRIM(entry_number) = ?', [trim($pen)])
                ->orderByDesc('id')
                ->get(['id']);

            if ($trainees->isEmpty()) {
                $notFound++;
                if ($isVerbose) {
                    $this->line("  NOT FOUND: {$pen}");
                }
                continue;
            }

            if ($isVerbose) {
                $this->line("  {$pen} → pe_fee:{$invAmt} fee_paid:{$feePaid} status:{$invStatus} paid:{$amtPaid} mop:{$mop}");
            }

            if (!$dry) {
                $payload = [
                    'invoice_amount' => $invAmt,
                    'fee_paid'       => $feePaid,
                    'invoice_status' => $invStatus,
                ];
                // amount_paid: only write when we have a value (column is NOT NULL in DB)
