                // Only overwrite these if the Excel has a value
                if ($mop     !== null) $payload['mode_of_payment'] = $mop;
                if ($payDate !== null) $payload['payment_date']    = $payDate;
                if ($sponsor !== null) $payload['sponsor']         = $sponsor;

                // Update ALL rows for this PEN (handles duplicates)
                DB::table('trainees')
                    ->whereIn('id', $trainees->pluck('id'))
                    ->update($payload);
            }
            $updated++;
        }

        $this->newLine();
        $this->info('--- Summary ---');
        if ($dry) {
            $this->warn("Would update : {$updated} trainee(s)");
        } else {
            $this->info("Updated      : {$updated} trainee(s)");
        }
        $this->line("Not found    : {$notFound} PE Number(s)");
        if ($notFound > 0) {
            $this->warn('Run with -v to see which PENs were not found.');
        }
        return self::SUCCESS;
    }
